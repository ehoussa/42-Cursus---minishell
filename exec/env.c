#include "../minishell.h"

int	env(char **env)
{
	int		i;
	char	*tmp;

	i = 0;
	while (env[i])
	{
		tmp = ft_strchr(env[i], '=');
		if (tmp)
			ft_putendl_fd(env[i], 1);
		i++;
	}
	return (0);
}
